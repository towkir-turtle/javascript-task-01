function sumMachine(num1, num2) {
  const sum = num1 + num2;
  return sum;
}
const addiction1 = sumMachine(3, 2);
console.log(addiction1);
const addiction2 = sumMachine(-3, -6);
console.log(addiction2);
const addiction3 = sumMachine(7, 3);
console.log(addiction3);

